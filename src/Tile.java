public class Tile {
    public int x;
    public int y;
    public int owner;
    public int num_dice;
    public int getX() {
        return x;
    }
    public void setX(int x) {
        this.x = x;
    }
    public int getY() {
        return y;
    }
    public void setY(int y) {
        this.y = y;
    }
    public int getOwner() {
        return owner;
    }
    public void setOwner(int owner) {
        this.owner = owner;
    }
    public int getNum_dice() {
        return num_dice;
    }
    public void setNum_dice(int num_dice) {
        this.num_dice = num_dice;
    }

}
